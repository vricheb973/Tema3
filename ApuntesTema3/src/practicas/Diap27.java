package practicas;

public class Diap27 {

	public static void main(String[] args) {
		/*Diseñar un programa que muestre la 
		 *suma de los 10 primeros números 
		 *impares empezando desde el 0.
		 */

		int n = 0;
	
		
		for(int i = 1;i <= 20;i += 2) {
			n = n + i;
		
			System.out.println(n);
			
		}
		
	}

}
